export const schedule = domainCampSchedule;

export default function Timetable({ scheduleData = domainCampSchedule, activeType = 'domain', onToggle }) {
  const [day, setDay] = React.useState(new Date().toLocaleDateString('en-US', { weekday: 'short' }));
  const days = ['Mon', 'Tue', 'Wed', 'Thu', 'Fri', 'Sat'];

  // Fallback to Mon if today is Sun or not in list
  const currentDay = days.includes(day) ? day : 'Mon';
  const [selectedDay, setSelectedDay] = React.useState(currentDay);

  return (
    <div className="bg-neutral-900 border border-neutral-800 rounded-xl overflow-hidden shadow-sm">
      <div className="p-6 border-b border-neutral-800 flex flex-col md:flex-row justify-between items-center gap-4">
        <div className="flex items-center gap-4">
          <h2 className="text-xl font-bold text-white">My Time Table</h2>
          {onToggle && (
            <div className="flex bg-neutral-800 rounded-lg p-1 border border-neutral-700">
              <button 
                onClick={() => onToggle('regular')}
                className={`px-3 py-1 text-xs font-medium rounded-md transition-all ${activeType === 'regular' ? 'bg-neutral-600 text-white shadow-sm' : 'text-neutral-400 hover:text-white'}`}
              >
                Regular
              </button>
              <button 
                onClick={() => onToggle('domain')}
                className={`px-3 py-1 text-xs font-medium rounded-md transition-all ${activeType === 'domain' ? 'bg-indigo-600 text-white shadow-sm' : 'text-neutral-400 hover:text-white'}`}
              >
                Domain Camp
              </button>
            </div>
          )}
        </div>
        <div className="flex gap-2 overflow-x-auto w-full md:w-auto pb-2 md:pb-0">
          {days.map(d => (
            <button 
              key={d} 
              onClick={() => setSelectedDay(d)}
              className={`px-4 py-2 rounded-full text-sm font-medium transition-colors whitespace-nowrap ${
                selectedDay === d 
                  ? 'bg-white text-black' 
                  : 'bg-neutral-800 text-neutral-400 hover:bg-neutral-700 hover:text-white'
              }`}
            >
              {d}
            </button>
          ))}
        </div>
      </div>
      
      <div className="overflow-x-auto">
        <table className="w-full text-left text-sm">
          <thead className="bg-neutral-950 text-neutral-500 font-medium border-b border-neutral-800">
            <tr>
              <th className="px-6 py-4 w-32">Time</th>
              <th className="px-6 py-4">Subject</th>
              <th className="px-6 py-4">Teacher</th>
              <th className="px-6 py-4 text-right">Room</th>
            </tr>
          </thead>
          <tbody className="divide-y divide-neutral-800">
            {scheduleData[selectedDay]?.length > 0 ? (
              scheduleData[selectedDay].map((slot, i) => (
                <tr key={i} className="hover:bg-neutral-800/50 transition-colors">
                  <td className="px-6 py-4 font-mono text-neutral-500 whitespace-nowrap">{slot.time}</td>
                  <td className="px-6 py-4">
                    <div className="font-bold text-white">{slot.subject}</div>
                    <div className="text-xs text-neutral-500 font-mono mt-0.5">{slot.code}</div>
                  </td>
                  <td className="px-6 py-4 text-neutral-400">{slot.teacher || '-'}</td>
                  <td className="px-6 py-4 text-right font-mono text-neutral-500">{slot.room}</td>
                </tr>
              ))
            ) : (
              <tr>
                <td colSpan={4} className="px-6 py-12 text-center text-neutral-500 italic">
                  No classes scheduled for this day.
                </td>
              </tr>
            )}
          </tbody>
        </table>
      </div>
    </div>
  );
}
